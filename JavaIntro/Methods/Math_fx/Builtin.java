package JavaIntro.Methods.Math_fx;

public class Builtin {
  public static void main(String[] args) {
    System.out.println(Math.sqrt(4));
    System.out.println(Math.pow(4, 4));
    System.out.println(Math.pow(-4.788, -4.78));
    System.out.println(Math.abs(-7.12));
    System.out.println(Math.floor(-7.12));
    System.out.println(Math.ceil(-7.12));
    System.out.println(Math.min(3, 9));
  }
}
